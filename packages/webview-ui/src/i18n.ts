export function getEffectiveLocale(language: FtpManagerLanguage, vscodeLanguage: string): Locale {
  if (language === 'fr' || language === 'en' || language === 'ja' || language === 'ko' || language === 'zh-cn') {
    return language;
  }

  const detectedLanguage = vscodeLanguage.toLowerCase();
  if (detectedLanguage.startsWith('fr')) return 'fr';
  if (detectedLanguage.startsWith('ja')) return 'ja';
  if (detectedLanguage.startsWith('ko')) return 'ko';
  if (detectedLanguage === 'zh-cn' || detectedLanguage.startsWith('zh')) return 'zh-cn';
  return 'en';
}

export function createTranslator(language: FtpManagerLanguage, vscodeLanguage: string) {
  const locale = getEffectiveLocale(language, vscodeLanguage);
  return messages[locale];
}
